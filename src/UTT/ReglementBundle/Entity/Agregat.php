<?php

namespace UTT\ReglementBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Agregat
 *
 * @ORM\Table(name="agregat")
 * @ORM\Entity(repositoryClass="UTT\ReglementBundle\Repository\AgregatRepository")
 */
class Agregat
{
    /**
     * @var int
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var string
     *
     * @ORM\Column(name="nom", type="string", length=255, unique=true)
     */
    private $nom;


    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set nom
     *
     * @param string $nom
     *
     * @return Agregat
     */
    public function setNom($nom)
    {
        $this->nom = $nom;

        return $this;
    }

    /**
     * Get nom
     *
     * @return string
     */
    public function getNom()
    {
        return $this->nom;
    }
}

